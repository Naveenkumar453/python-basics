print(name.upper()) #Conversts whole string into upper case
print(name.lower()) #conversts whole string into lower case
print(name.isdigit()) # check the value is digit or character
print(name.isalpha()) #should not give space to become true
print(name.count("e")) #no. of characters or  no of intergers
print(name.replace("o", "0")) #Replace String
print(name*3)
print(name.split(", ")) #splits the string



